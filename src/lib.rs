/*!

# glium

Graphics stack for Rust

The `glium` packages includes a group of libraries:

 - `glium-core` is the lowest-level library and allows you to directly interface with OpenGL.
 - `glium-sprite2d` allows you to draw 2D sprites on the screen.
 - `glium-image` provides an utility to turn images into texture.
 - `glium-text` draws text on the screen.

Some others are not part of the `glium` package but can be imported if needed:

 - `glium-spine` draws [Spine](http://esotericsoftware.com/) animations on the screen.

*/
